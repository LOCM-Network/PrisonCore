package com.locm.core.ces.obj;

public class RealCE {

	private int levelOfCe;
	private String displayNameOfCe;

	public RealCE(int levelOfCe, String displayNameOfCe) {
		this.levelOfCe = levelOfCe;
		this.displayNameOfCe = displayNameOfCe;
	}

	public int getLevelOfCe() {
		return levelOfCe;
	}

	public String getDisplayNameOfCe() {
		return displayNameOfCe;
	}

}
